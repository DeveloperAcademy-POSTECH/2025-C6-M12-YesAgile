 [File Name] : JourneyMapMarkerView.swift
 [Role]      : 지도 위에 표시될 개별 '사진 마커(Pin)'의 디자인 정의
 [Layer]     : View (Sub-Component)
 ===================================================================================
 
 [핵심 책임 (Core Responsibilities)]
 1. **Image Rendering**: 전달받은 URL을 사용하여 `CachedAsyncImage`로 이미지를 그립니다.
 2. **Styling**: 이미지를 원형으로 자르고(Crop), 테두리(Stroke)와 그림자(Shadow)를 적용하여 '지도 핀'처럼 보이게 만듭니다.
 
 [설계 의도 및 이유 (Why)]
 1. 왜 별도 파일로 분리했는가?
    - `JourneyMapView` 안에 마커 디자인 코드를 섞으면 코드가 지저분해집니다.
    - 나중에 "핀 디자인을 네모로 바꿔주세요" 같은 수정 사항이 왔을 때, 이 파일만 고치면 되므로 유지보수가 쉽습니다.
 
 2. 비유 (Metaphor): "스티커"
    - 이 뷰는 지도라는 거대한 도화지 위에 붙이는 '작은 원형 스티커'입니다.
    - 스티커는 자신이 어디에 붙는지(좌표)는 모르고, 오직 "내 얼굴(사진)이 어떻게 생겼는지"만 신경 씁니다.
 */

    
