     3. `.clipShape(Circle())`: 원형으로 자릅니다.
     4. `.overlay(...)`: 흰색 테두리를 둘러 핀 느낌을 냅니다.
     5. `.shadow(...)`: 그림자를 주어 지도에서 떠 있는 느낌을 줍니다.
     6. `.frame(...)`: 크기는 너무 크지 않게 (예: 40x40) 고정합니다.
